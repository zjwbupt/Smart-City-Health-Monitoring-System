    void init_work(){
        for (Long x : Datastore.getInstance().getDoctors().keySet()){
            Doctor tmpd = Datastore.getInstance().getDoctors().get(x);
            //System.out.println(tmpr.getHospital() + hospital.getName());
            if (tmpd.getHospital().equals(hospital.getName()) == true){
                if (doctor_list.contains(tmpd)==false){
                    doctor_list.add(tmpd);
                }
            }
        }
        hospital.getDoctorList().getDoctorList().clear();
        hospital.getDoctorList().getDoctorList().addAll(doctor_list);
    }
    
    public void populate(){
        DefaultTableModel dtm1 = (DefaultTableModel)DoctorTable.getModel();
        dtm1.setRowCount(0);
        
        for(Doctor a : hospital.getDoctorList().getDoctorList()){
            Object[] row = new Object[dtm1.getColumnCount()];
            row[0] = a;
            row[1] = a.getId();
            row[2] = String.valueOf(a.getRecordHistory().getSize());
            dtm1.addRow(row);
        }
    }
