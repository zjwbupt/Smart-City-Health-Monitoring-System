    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        
        String s1 = NameText.getText();
        String s2 = IDText.getText();
        
        DefaultTableModel dtm1 = (DefaultTableModel)DoctorTable.getModel();
        dtm1.setRowCount(0);
        
        for(Doctor a : hospital.getDoctorList().getDoctorList()){
            String Dname = a.getFirstName()+ " " + a.getLastName();
            if (s1.equals("")==false&&Dname.contains(s1)==false) continue;
            if (s2.equals("")==false&&Long.toString(a.getId()).contains(s2)==false) continue;
            Object[] row = new Object[dtm1.getColumnCount()];
            row[0] = a;
            row[1] = a.getId();
            row[2] = String.valueOf(a.getRecordHistory().getSize());
            dtm1.addRow(row);
        }
        
    }//GEN-LAST:event_SearchBtnActionPerformed

