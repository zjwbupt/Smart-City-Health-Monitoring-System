     public void init_work(){
        hospital.getRecodeList().getRecodeList().clear();
        for (int x : Datastore.getInstance().getRecords().keySet()){
            Recode tmpr = Datastore.getInstance().getRecords().get(x);
            if (tmpr.getHospital().equals(hospital.getName()) == true){
                    hospital.getRecodeList().add(tmpr);
            }
        }
        //System.out.println(disease_list.size()+"~~~~~~~~~~~~~~~~");
    }
    
    public void populate(){
        DefaultTableModel dtm1 = (DefaultTableModel)RecodeTabel.getModel();
        dtm1.setRowCount(0);
        
        for(Recode a : hospital.getRecodeList().getRecodeList()){
            Object[] row = new Object[dtm1.getColumnCount()];
            row[0] = a;
            row[1] = Datastore.getInstance().getPatients().get(a.getPID()).getFirstName() + " " + Datastore.getInstance().getPatients().get(a.getPID()).getLastName();
            row[2] = a.getPID();
            row[3] = Datastore.getInstance().getDoctors().get(a.getDID()).getFirstName() + " " +Datastore.getInstance().getDoctors().get(a.getDID()).getLastName();
            row[4] = Long.toString(a.getDID());
            row[5] = a.getDisName();
            row[6] = a.getDate();
            dtm1.addRow(row);
        }
    } 
     
