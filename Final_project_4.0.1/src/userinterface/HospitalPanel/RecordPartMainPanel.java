    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        String s1 = PNameText.getText();
        String s2 = DNameText.getText();
        String s3 = DisNameText.getText();
        String s4 = MINDText.getText();
        String s5 = MAXDText.getText();
        
        DefaultTableModel dtm1 = (DefaultTableModel)RecodeTabel.getModel();
        dtm1.setRowCount(0);
        
        for(Recode a : hospital.getRecodeList().getRecodeList()){
            String Pname = Datastore.getInstance().getPatients().get(a.getPID()).getFirstName() + " " + Datastore.getInstance().getPatients().get(a.getPID()).getLastName();
            String Dname = Datastore.getInstance().getDoctors().get(a.getDID()).getFirstName() + " " +Datastore.getInstance().getDoctors().get(a.getDID()).getLastName();
            if (s1.equals("")==false&&Pname.contains(s1)==false) continue;
            if (s2.equals("")==false&&Dname.contains(s2)==false) continue;
            if (s3.equals("")==false&&a.getDisName().contains(s3) == false) continue;
            if (s4.equals("")==false&&a.getDate().compareTo(s4)<0) continue;
            if (s5.equals("")==false&&a.getDate().compareTo(s4)>0) continue;
            Object[] row = new Object[dtm1.getColumnCount()];
            row[0] = a;
            row[1] = Pname;
            row[2] = a.getPID();
            row[3] = Dname;
            row[4] = Long.toString(a.getDID());
            row[5] = a.getDisName();
            row[6] = a.getDate();
            dtm1.addRow(row);
        }
    }//GEN-LAST:event_SearchBtnActionPerformed

